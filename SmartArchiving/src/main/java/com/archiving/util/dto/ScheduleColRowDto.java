package com.archiving.util.dto;

public class ScheduleColRowDto {
	private int colSeq;
	private String colCd;
	private String colNm;
	private String colTypeCd;
	private int colLen;
	private String colDateYn;
	private String colEncYn;

	public int getColSeq() {
		return colSeq;
	}

	public void setColSeq(int colSeq) {
		this.colSeq = colSeq;
	}

	public String getColCd() {
		return colCd;
	}

	public void setColCd(String colCd) {
		this.colCd = colCd;
	}

	public String getColNm() {
		return colNm;
	}

	public void setColNm(String colNm) {
		this.colNm = colNm;
	}

	public String getColTypeCd() {
		return colTypeCd;
	}

	public void setColTypeCd(String colTypeCd) {
		this.colTypeCd = colTypeCd;
	}

	public int getColLen() {
		return colLen;
	}

	public void setColLen(int colLen) {
		this.colLen = colLen;
	}

	public String getColDateYn() {
		return colDateYn;
	}

	public void setColDateYn(String colDateYn) {
		this.colDateYn = colDateYn;
	}

	public String getColEncYn() {
		return colEncYn;
	}

	public void setColEncYn(String colEncYn) {
		this.colEncYn = colEncYn;
	}
}
